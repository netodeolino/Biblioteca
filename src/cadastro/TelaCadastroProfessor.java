    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
         String cpfaux = txtCpf.getText();
        String nomeaux = txtNome.getText();
        String ruaaux = txtRua.getText();
        String numaux = txtNum.getText();
        int novonum = Integer.parseInt(numaux);
        String bairroaux = txtBairro.getText();
        String cidaaux = txtCidade.getText();
        String regaux = txtRegistro.getText();
        int novomatri = Integer.parseInt(regaux);
        
        Pessoa p = new Pessoa();
        p.setCpf(cpfaux);
        p.setNome(nomeaux);
        p.setRua(ruaaux);
        p.setNumero(novonum);
        p.setBairro(bairroaux);
        p.setCidade(cidaaux);
        
        RepositorioPessoa repP = new PessoaDAO();
        repP.cadastrarPessoa(p);
        
        Professor pr = new Professor();
        pr.setCpf(cpfaux);
        pr.setRegistro(novomatri);
        
        RepositorioProfessor repPR = new ProfessorDAO();
        repPR.cadastrarProfessor(pr);
    }//GEN-LAST:event_btnCadastrarActionPerformed

